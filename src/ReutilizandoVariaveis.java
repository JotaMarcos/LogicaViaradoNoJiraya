public class ReutilizandoVariaveis {
    public static void main(String[] args) {
        System.out.println("=========================== Calcular o Valor de cada Porcentagem =========================== ");

        double salario = 5000;

        double resultado = salario * 0.3;
        System.out.println("O Valor do Resultado de 30% é igual a: " +  resultado);

        resultado = salario * 0.15;
        System.out.println("O Valor do Resultado de 15% é igual a: " +  resultado);

        resultado = salario * 0.05;
        System.out.println("O Valor do Resultado de 5% é igual a: " +  resultado);

        System.out.println("============================ Procedimento Realizado com Sucesso ============================ ");

    }
}
