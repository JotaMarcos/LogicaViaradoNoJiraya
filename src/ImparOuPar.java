public class ImparOuPar {
    public static void main(String[] args) {
        int numero = 2;
        if(numero % 2 == 0){
            System.out.println("O Valor do Número " + (numero % 2) + " é Par!!!");
        }else{
            System.out.println("O Valor do Número " + (numero % 2) + " é Impar!!!");
        }
    }

}
